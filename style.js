/*Nav Bar Animation*/
function ThreeDothide(){
  x =  document.getElementById("h_o_left_area");
  x.style.marginLeft = "0";
  document.getElementById("h_o_right_area").style.left = "20%";
}


function darkLeftOnclick(){
  x =  document.getElementById("h_o_left_area");
  x.style.marginLeft = "-20%";
  document.getElementById("h_o_right_area").style.left = "100%";
}
function OcloseLeft(){
  x =  document.getElementById("h_o_left_area");
  x.style.marginLeft = "-20%";
  document.getElementById("h_o_right_area").style.left = "100%";
}
function OcloseRight(){
  x =  document.getElementById("h_o_left_area");
  x.style.marginLeft = "-20%";
  document.getElementById("h_o_right_area").style.left = "100%";
}
